print("--- Bài 3 ---")
a = int(input("Nhập số nguyên a: "))
b = int(input("Nhập số nguyên b: "))
c = int(input("Nhập số nguyên c: "))

# a) Tổng và tích
print("Tổng ba số: ", a + b + c)
print("Tích ba số: ", a * b * c)

# b) Hiệu của 2 số bất kỳ (ví dụ a và b)
print("Hiệu của a và b: ", a - b)

# c) Chia lấy nguyên, phần dư và kết quả chính xác
print("Chia lấy phần nguyên (a // b): ", a // b)
print("Chia lấy phần dư (a % b): ", a % b)
print("Kết quả chính xác (a / b): ", a / b)